class RollSlotWidget extends StatelessWidget {
  final List<String> emojiList;

  final RollSlotController rollSlotController;

  const RollSlotWidget({Key key, this.emojiList, this.rollSlotController})
      : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Flexible(
      child: Column(
        mainAxisAlignment: MainAxisAlignment.center,
        children: <Widget>[
          Flexible(
            child: Padding(
              padding: const EdgeInsets.all(8.0),
              child: RollSlot(
                  duration: Duration(milliseconds: 10000),
                  itemExtend: 300,
                  shuffleList: false,
                  rollSlotController: rollSlotController,
                  children: emojiList.map(
                    (e) {
                      return BuildItem(
                        emoji: e,
                      );
                    },
                  ).toList()),
            ),
          ),
        ],
      ),
    );
  }
}

class BuildItem extends StatelessWidget {
  const BuildItem({
    Key key,
    this.index,
    this.emoji,
  }) : super(key: key);

  final int index;
  final String emoji;

  @override
  Widget build(BuildContext context) {
    return Container(
      decoration: BoxDecoration(
          color: Colors.transparent,
          borderRadius: BorderRadius.circular(20),
          border: Border.all()),
      alignment: Alignment.center,
      child: false
          ? Image.network(
              'https://emojipedia-us.s3.dualstack.us-west-1.amazonaws.com/thumbs/160/apple/271/grinning-face_1f600.png',
              height: 200,
              width: 300,
              fit: BoxFit.fill,
            )
          : Center(
              child: Text(
                emoji,
                key: Key(emoji),
                style: const TextStyle(fontSize: 100),
              ),
            ),
    );
  }
}
